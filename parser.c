#include "shell.h"
/**
 * is_cmd - a function that checks whether a file is executable or not
 * @info: represents the structure of information
 * @path: the pathway to the desired file
 * Return: either 1 for true or 0 for false
 **/
int is_cmd(info_t *info, char *path)
{
	struct stat st;

	(void)info;
	if (!path || stat(path, &st))
		return (0);

	if (st.st_mode & S_IFREG)
	{
		return (1);
	}
	return (0);
}

/**
 * dup_chars - a function that creates a copy of characters
 * @pathstr: the pathway to dessired string
 * @start: the index that marks the starting point
 * @stop: the index that marks the stoppint point
 * Return: the memmory allocated for a fresh buf
 **/
char *dup_chars(char *pathstr, int start, int stop)
{
	static char buf[1024];
	int i = 0, k = 0;

	for (k = 0, i = start; i < stop; i++)
		if (pathstr[i] != ':')
			buf[k++] = pathstr[i];
	buf[k] = 0;
	return (buf);
}

/**
 * find_path - locates this command within the pathway to the string
 * @info:  the pathway to dessired stucture
 * @pathstr: the pathway to dessired string
 * @cmd: the desired command
 * Return: the pathway to the command
 **/
char *find_path(info_t *info, char *pathstr, char *cmd)
{
	int i = 0, curr_pos = 0;
	char *path;

	if (!pathstr)
		return (NULL);
	if ((_strlen(cmd) > 2) && starts_with(cmd, "./"))
	{
		if (is_cmd(info, cmd))
			return (cmd);
	}
	while (1)
	{
		if (!pathstr[i] || pathstr[i] == ':')
		{
			path = dup_chars(pathstr, curr_pos, i);
			if (!*path)
				_strcat(path, cmd);
			else
			{
				_strcat(path, "/");
				_strcat(path, cmd);
			}
			if (is_cmd(info, path))
				return (path);
			if (!pathstr[i])
				break;
			curr_pos = i;
		}
		i++;
	}
	return (NULL);
}
